package com.project.ayosiram3;

public class RiwayatData {
    private String key;
    private String child;

    public RiwayatData(String key, String child) {
        this.key = key;
        this.child = child;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getChild() {
        return child;
    }

    public void setChild(String child) { this.child = child; }

}